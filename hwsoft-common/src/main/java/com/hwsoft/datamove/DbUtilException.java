package com.hwsoft.datamove;

import com.hwsoft.exception.BaseException;

public class DbUtilException extends BaseException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 982595667960084168L;

	public DbUtilException() {
		super();
	}

	public DbUtilException(String message, Throwable cause) {
		super(message, cause);
	}

	public DbUtilException(String message) {
		super(message);
	}

	public DbUtilException(Throwable cause) {
		super(cause);
	}

}
