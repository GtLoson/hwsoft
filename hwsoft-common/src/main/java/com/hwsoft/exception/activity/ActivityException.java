package com.hwsoft.exception.activity;

import com.hwsoft.exception.BaseException;

/**
 * @author tzh
 *
 */
public class ActivityException extends BaseException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6643816326153746780L;

	public ActivityException() {
		super();
	}

	public ActivityException(String message, Throwable cause) {
		super(message, cause);
	}

	public ActivityException(String message) {
		super(message);
	}

	public ActivityException(Throwable cause) {
		super(cause);
	}

}
