package com.hwsoft.exception.staff;

import com.hwsoft.exception.BaseException;

/**
 * @author tzh
 *
 */
public class StaffException extends BaseException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 2963046185115012228L;

	public StaffException() {
		super();
	}

	public StaffException(String message, Throwable cause) {
		super(message, cause);
	}

	public StaffException(String message) {
		super(message);
	}

	public StaffException(Throwable cause) {
		super(cause);
	}

}
