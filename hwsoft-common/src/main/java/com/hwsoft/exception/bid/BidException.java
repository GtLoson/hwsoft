package com.hwsoft.exception.bid;

import com.hwsoft.exception.BaseException;

public class BidException extends BaseException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3352912715071869452L;

	public BidException() {
		super();
	}

	public BidException(String message, Throwable cause) {
		super(message, cause);
	}

	public BidException(String message) {
		super(message);
	}

	public BidException(Throwable cause) {
		super(cause);
	}

}
