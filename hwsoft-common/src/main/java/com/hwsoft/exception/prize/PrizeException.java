package com.hwsoft.exception.prize;

import com.hwsoft.exception.BaseException;

/**
 *
 */
public class PrizeException extends BaseException{

	/**
	 *
	 */
	private static final long serialVersionUID = 2963046185115012228L;

	public PrizeException() {
		super();
	}

	public PrizeException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrizeException(String message) {
		super(message);
	}

	public PrizeException(Throwable cause) {
		super(cause);
	}

}
