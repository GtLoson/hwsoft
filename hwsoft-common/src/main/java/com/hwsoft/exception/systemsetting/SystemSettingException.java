package com.hwsoft.exception.systemsetting;

import com.hwsoft.exception.BaseException;

/**
 * @author tzh
 *
 */
public class SystemSettingException extends BaseException {

	/**
	 *
	 */
	private static final long serialVersionUID = -7733675079468852939L;

	public SystemSettingException() {
		super();
	}

	public SystemSettingException(String message, Throwable cause) {
		super(message, cause);
	}

	public SystemSettingException(String message) {
		super(message);
	}

	public SystemSettingException(Throwable cause) {
		super(cause);
	}

}
