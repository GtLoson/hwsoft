package com.hwsoft.exception.repayment;

import com.hwsoft.exception.BaseException;

public class RepaymentException extends BaseException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7473510991842446713L;

	public RepaymentException() {
		super();
	}

	public RepaymentException(String message, Throwable cause) {
		super(message, cause);
	}

	public RepaymentException(String message) {
		super(message);
	}

	public RepaymentException(Throwable cause) {
		super(cause);
	}

}
