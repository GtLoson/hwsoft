package com.hwsoft.exception.apphelp;

import com.hwsoft.exception.BaseException;

/**
 * 应用帮助异常
 */
public class AppHelpException extends BaseException{

	/**
	 *
	 */
	private static final long serialVersionUID = 2963046185115012229L;

	public AppHelpException() {
		super();
	}

	public AppHelpException(String message, Throwable cause) {
		super(message, cause);
	}

	public AppHelpException(String message) {
		super(message);
	}

	public AppHelpException(Throwable cause) {
		super(cause);
	}

}
