package com.hwsoft.exception.lending;

import com.hwsoft.exception.BaseException;

public class LendingException extends BaseException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2580002126045613732L;

	public LendingException() {
		super();
	}

	public LendingException(String message, Throwable cause) {
		super(message, cause);
	}

	public LendingException(String message) {
		super(message);
	}

	public LendingException(Throwable cause) {
		super(cause);
	}

}
